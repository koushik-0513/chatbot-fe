export const EmojiPicker = ({ onEmojiSelect, onClose }: TEmojiPickerProps) => {
  const [selectedCategory, setSelectedCategory] = useState(0);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    onClose();
  };

  return (
    <div className="absolute bottom-16 left-0 z-50 w-80 rounded-lg border border-gray-200 bg-white shadow-lg">
      {/* Header */}
      <div className="flex items-center justify-between border-b border-gray-200 p-3">
        <h3 className="text-sm font-medium text-gray-700">Choose an emoji</h3>
        <button
          onClick={onClose}
          className="rounded-full p-1 transition-colors hover:bg-gray-100"
        >
          <span className="text-gray-500">Ã—</span>
        </button>
      </div>

      {/* Category tabs */}
      <div className="flex border-b border-gray-200">
        {emojiCategories.map((category, index) => (
          <button
            key={index}
            onClick={() => setSelectedCategory(index)}
            className={`flex-1 px-3 py-2 text-xs font-medium transition-colors ${
              selectedCategory === index
                ? "border-b-2 border-blue-600 bg-blue-50 text-blue-600"
                : "text-gray-600 hover:bg-gray-50"
            }`}
          >
            {category.name.split(" ")[0]}
          </button>
        ))}
      </div>

      {/* Emoji grid */}
      <div className="max-h-64 overflow-y-auto p-3">
        <div className="grid grid-cols-8 gap-1">
          {emojiCategories[selectedCategory].emojis.map((emoji, index) => (
            <button
              key={index}
              onClick={() => handleEmojiClick(emoji)}
              className="flex h-8 w-8 items-center justify-center rounded text-lg transition-colors hover:bg-gray-100"
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
};
